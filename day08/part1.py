import os.path

import pytest

INPUT_TXT = os.path.join(os.path.dirname(__file__), 'input.txt')


def compute(s: str) -> int:
    lines = s.splitlines()
    output_digits_display = [line.split("| ")[1].split() for line in lines]

    count = 0
    for output_digits in output_digits_display:
        count += sum(len(digit) in [2, 3, 4, 7] for digit in output_digits)

    return count


